    private void tblItemsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblItemsMouseClicked
        int index = model.getIndex(tblItems.getSelectedRow());
        Item item = new Item();
        try { // catch out of range (blank row)
            item = model.getData().get(index);
        } catch (Exception e) {
            btnNewActionPerformed(null);
            return;
        }
        txtCode.setText(item.getCode());
        txtCode.setEnabled(false);
        txtName.setText(item.getName());
        txtUnit.setText(item.getUnit());
        txtPrice.setText(Integer.toString(item.getPrice()));
        boxSupplying.setSelected(item.isSupplying());
        String supCode = item.getSupCode();
        cbbSupplier.setSelectedIndex(supplierData.indexOf(new Supplier(supCode)));
        addNew = false;
    }//GEN-LAST:event_tblItemsMouseClicked

