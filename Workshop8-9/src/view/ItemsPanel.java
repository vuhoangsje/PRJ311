    public boolean validateItem(String code, String price) {
        if (!code.matches("E\\d{4}")) {
            JOptionPane.showMessageDialog(this, "Item code must be Exxx");
            return false;
        };
        try {
            Integer.parseInt(price);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Price must be integer");
            return false;
        }
        return true;
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String code = txtCode.getText();
        String name = txtName.getText();
        String unit = txtUnit.getText();
        String price = txtPrice.getText();
        Supplier sup = (Supplier) cbbSupplier.getSelectedItem();
        boolean supplying = boxSupplying.isSelected();
        if (!validateItem(code, price)) {
            return;
        }
        Item item = new Item(code, name, sup.getCode(), unit, Integer.parseInt(price), supplying);
        try {
            if (itemService.getItemByCode(code) != null) {
                JOptionPane.showMessageDialog(this, "This code is already exists");
                return;
            }
            if (addNew) {
                itemService.insertItem(item);
                model.getData().add(item);
            } else {
                itemService.updateItem(item);
                int pos = model.getIndex(tblItems.getSelectedRow());
                model.getData().set(pos, item);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        changed = true;
        updatePage();
        tblItems.updateUI();
        btnNewActionPerformed(null);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (addNew) //fix mouse click on blank row then click remove
        {
            return;
        }
        int r = JOptionPane.showConfirmDialog(this, "Are you sure?", "remove?", JOptionPane.YES_OPTION);
        if (r == JOptionPane.YES_OPTION) {
            try {
                if (itemService.getItemByCode(txtCode.getText()) == null) {
                    JOptionPane.showMessageDialog(this, "Something wrong with database! Please restart porgram");
                    return;
                }
                itemService.removeItem(txtCode.getText());
                int index = model.getIndex(tblItems.getSelectedRow());
                model.getData().remove(index);
                btnNewActionPerformed(null);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        changed = true;
        updatePage();
        tblItems.updateUI();
        btnNewActionPerformed(null);
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (txtSearch.getText().trim().equals("")) {
            loadData();
            tblItems.updateUI();
        } else {
            try {
                model.getData().removeAllElements();
                Vector<Item> list = itemService.getItemByName(txtSearch.getText().trim());
                for (Item it : list) {
                    model.getData().add(it);
                }
                tblItems.updateUI();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed


