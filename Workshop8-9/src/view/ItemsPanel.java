    public void loadSuppliers() {
        cbbSupplier.removeAllItems();
        try {
            supplierData = supplierService.getAllSupplier();
            for (Supplier x : supplierData) {
                cbbSupplier.addItem(x);
            }
            updateUI();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void updatePage() {
        if (model.getMaxPage() == -1) {
            txtPage.setText("1/1");
            btnPreviousPage.setEnabled(false);
            btnNextPage.setEnabled(false);
            return;
        }
        txtPage.setText(String.format("%d/%d", model.getCurrentPage() + 1, model.getMaxPage() + 1));
        if (model.getCurrentPage() == 0) {
            btnPreviousPage.setEnabled(false);
        } else {
            btnPreviousPage.setEnabled(true);
        }
        if (model.getCurrentPage() == model.getMaxPage()) {
            btnNextPage.setEnabled(false);
        } else {
            btnNextPage.setEnabled(true);
        }
    }

    void loadData() {
        try {
            model.getData().removeAllElements();
            Vector<Item> list = itemService.getAllItem();
            for (Item it : list) {
                model.getData().add(it);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

